def set_window(x0, y0, x1, y1):
    command(0x2A)
    data([x0 >> 8, x0 & 0xFF, x1 >> 8, x1 & 0xFF])
    command(0x2B)
    data([y0 >> 8, y0 & 0xFF, y1 >> 8, y1 & 0xFF])
    command(0x2C)

def fill(color):
    set_window(0, 0, 239, 239)
    hi = (color >> 8) & 0xFF
    lo = color & 0xFF
    GPIO.output(DC, 1)
    chunk = [hi, lo] * 1024
    pixels = 240 * 240
    full_chunks = pixels // 1024
    rem = pixels % 1024

    for _ in range(full_chunks):
        spi.writebytes(chunk)
    if rem:
        spi.writebytes([hi, lo] * rem)

init()

fill(0xF800)  # red
time.sleep(2)
fill(0x07E0)  # green
time.sleep(2)
fill(0x001F)  # blue
time.sleep(2)
fill(0xFFE0)  # yellow

while True:
    time.sleep(1)
